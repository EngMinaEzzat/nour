import { describe, it, expect, beforeAll, afterAll } from "vitest";
import path from "path";
import {
  request, app, uid, createTestMerchant, cleanupTenant,
} from "./helpers.js";

describe("Uploads — Image Upload", () => {
  let ctx: Awaited<ReturnType<typeof createTestMerchant>>;

  beforeAll(async () => { ctx = await createTestMerchant(); });
  afterAll(async () => { await cleanupTenant(ctx.tenantId, ctx.merchantId); });

  it("✅ upload valid PNG image returns URL", async () => {
    // Create a minimal valid 1x1 PNG in-memory
    const pngHeader = Buffer.from([
      0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, // PNG signature
      0x00, 0x00, 0x00, 0x0D, 0x49, 0x48, 0x44, 0x52, // IHDR chunk
      0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01, // 1x1
      0x08, 0x02, 0x00, 0x00, 0x00, 0x90, 0x77, 0x53, // 8-bit RGB
      0xDE, 0x00, 0x00, 0x00, 0x0C, 0x49, 0x44, 0x41, // IDAT chunk
      0x54, 0x08, 0xD7, 0x63, 0xF8, 0xCF, 0xC0, 0x00,
      0x00, 0x00, 0x02, 0x00, 0x01, 0xE2, 0x21, 0xBC,
      0x33, 0x00, 0x00, 0x00, 0x00, 0x49, 0x45, 0x4E, // IEND chunk
      0x44, 0xAE, 0x42, 0x60, 0x82,
    ]);

    const res = await ctx.agent
      .post("/api/uploads/image")
      .attach("image", pngHeader, "test.png");
    expect(res.status).toBe(200);
    expect(res.body).toHaveProperty("url");
    const u = res.body.url as string;
    expect(u.startsWith("/api/uploads/") || u.includes("res.cloudinary.com")).toBe(true);
    expect(res.body).toHaveProperty("filename");
  });

  it("✅ upload valid BMP image returns URL with BMP extension", async () => {
    const bmp = Buffer.from([
      0x42, 0x4d, 0x3a, 0x00, 0x00, 0x00, 0x00, 0x00,
      0x00, 0x00, 0x36, 0x00, 0x00, 0x00, 0x28, 0x00,
      0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01, 0x00,
      0x00, 0x00, 0x01, 0x00, 0x18, 0x00, 0x00, 0x00,
      0x00, 0x00, 0x04, 0x00, 0x00, 0x00, 0x13, 0x0b,
      0x00, 0x00, 0x13, 0x0b, 0x00, 0x00, 0x00, 0x00,
      0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0xff, 0xff,
      0xff, 0x00,
    ]);

    const res = await ctx.agent
      .post("/api/uploads/image")
      .attach("image", bmp, "test.bmp");
    expect(res.status).toBe(200);
    const u = res.body.url as string;
    expect(/^\/api\/uploads\/.+\.bmp$/.test(u) || u.includes("res.cloudinary.com")).toBe(true);
  });

  it("❌ upload without auth returns 401", async () => {
    const pngHeader = Buffer.from([
      0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A,
      0x00, 0x00, 0x00, 0x0D, 0x49, 0x48, 0x44, 0x52,
      0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01,
      0x08, 0x02, 0x00, 0x00, 0x00, 0x90, 0x77, 0x53,
      0xDE, 0x00, 0x00, 0x00, 0x0C, 0x49, 0x44, 0x41,
      0x54, 0x08, 0xD7, 0x63, 0xF8, 0xCF, 0xC0, 0x00,
      0x00, 0x00, 0x02, 0x00, 0x01, 0xE2, 0x21, 0xBC,
      0x33, 0x00, 0x00, 0x00, 0x00, 0x49, 0x45, 0x4E,
      0x44, 0xAE, 0x42, 0x60, 0x82,
    ]);

    const res = await request(app)
      .post("/api/uploads/image")
      .attach("image", pngHeader, "test.png");
    expect(res.status).toBe(401);
  });

  it("❌ upload non-image file returns 415", async () => {
    const textFile = Buffer.from("This is not an image file");
    const res = await ctx.agent
      .post("/api/uploads/image")
      .attach("image", textFile, "fake.txt");
    expect(res.status).toBe(415);
  });

  it("❌ upload without file returns 400", async () => {
    const res = await ctx.agent.post("/api/uploads/image");
    expect(res.status).toBe(400);
  });
});
